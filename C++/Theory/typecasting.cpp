#include<iostream>
using namespace std;
// ----------------------------TYPE CASTING---------------------
int main()
{
    int a = 2;
    float b = 2.5;
    cout<<"(float)a= "<<(float)a<<endl;
    cout<<"float(a) ="<<float(a)<<'\n';
    cout<<"(int)b ="<<(int)b<<endl;
    cout<<"int(b) ="<<int(b)<<endl;
    return 0; 
}